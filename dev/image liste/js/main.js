
var photos = document.querySelectorAll('#photo-list li');

for (var i=0; i<photos.length; i++){

	photos[i].addEventListener('click',changeColor);
}


function changeColor(){
	
	
	this.classList.toggle("selected")
	document.querySelector("em").textContent = compte()

}

function compte(){

	return document.querySelectorAll(".selected").length
}

document.querySelector("em").textContent = compte()

